using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Forms;

namespace FindStringInFile
{
    static class Program
    {

        public static Config ProgConfig { get; set; }

        /// <summary>
        /// Der Haupteinstiegspunkt für die Anwendung.
        /// </summary>
        [STAThread]
        static void Main()
        {
            Application.EnableVisualStyles();
            Application.SetCompatibleTextRenderingDefault(false);

            ProgConfig = Config.LoadConfig();

            Application.Run(new FindStringInFileForm());


            if (!string.IsNullOrWhiteSpace(ProgConfig.ProgSavePath))
            {
                Config.ConfigSave(ProgConfig.ProgSavePath, ProgConfig,string.Empty);
            }
        }
    }
}
